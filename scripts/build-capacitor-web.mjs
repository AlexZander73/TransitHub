import { cp, mkdir, rm, stat, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = path.resolve(SCRIPT_DIR, "..");
const OUT_DIR = path.join(ROOT_DIR, "mobile", "www");

const STATIC_ENTRIES = [
  ".nojekyll",
  "index.html",
  "about.html",
  "alerts.html",
  "data.html",
  "how-it-works.html",
  "routes.html",
  "stops.html",
  "assets",
  "data"
];

async function exists(targetPath) {
  try {
    await stat(targetPath);
    return true;
  } catch {
    return false;
  }
}

async function copyEntry(name) {
  const source = path.join(ROOT_DIR, name);
  const target = path.join(OUT_DIR, name);
  if (!(await exists(source))) {
    console.warn(`[build:cap:web] Skipping missing entry: ${name}`);
    return;
  }
  await cp(source, target, { recursive: true, force: true });
}

async function build() {
  await rm(OUT_DIR, { recursive: true, force: true });
  await mkdir(OUT_DIR, { recursive: true });

  await Promise.all(STATIC_ENTRIES.map((entry) => copyEntry(entry)));

  const readme = [
    "This directory is generated by scripts/build-capacitor-web.mjs",
    "Do not edit files in this folder directly."
  ].join("\n");
  await writeFile(path.join(OUT_DIR, "README.txt"), `${readme}\n`, "utf8");
  await writeFile(path.join(OUT_DIR, ".gitkeep"), "", "utf8");

  console.log(`[build:cap:web] Copied ${STATIC_ENTRIES.length} entries to ${OUT_DIR}`);
}

build().catch((error) => {
  console.error("[build:cap:web] Failed:", error);
  process.exitCode = 1;
});
